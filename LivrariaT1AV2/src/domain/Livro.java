package domain;

public class Livro {
	
	String titulo;
	int ano;
	String isbn;
	
	public String getTitulo() {
	return titulo;
	}
	public void setTitulo(String titulo) {
	this.titulo = titulo;
	}
	public int getAno() {
	return ano;
	}
	public void setAno(int ano) {
	this.ano = ano;
	}
	public String getIsbn() {
	return isbn;
	}
	public void setIsbn(String isbn) {
	this.isbn = isbn;
	}

}
